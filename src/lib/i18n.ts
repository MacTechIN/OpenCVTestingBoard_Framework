export function useLanguage() {
  const [language, setLanguage] = useState<LanguageCode>('ko');

  useEffect(() => {
    const browserLang = navigator.language.slice(0, 2);
    if (Object.keys(UI_STRINGS.appName).includes(browserLang)) {
      setLanguage(browserLang as LanguageCode);
    }
  }, []);

  const t = (key: string): string => {
    return UI_STRINGS[key]?.[language] || UI_STRINGS[key]?.['en'] || key;
  };

  const translate = (data: Translatable): string => {
    if (typeof data === 'string') return data;
    return data[language] || data['en'] || Object.values(data)[0] || '';
  };

  return { language, setLanguage, t, translate };
}
